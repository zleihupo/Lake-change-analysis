def clean_area(val):
    """Extract numeric value from string, convert to float. Otherwise return NaN."""
    if pd.isna(val):
        return np.nan
    m = re.search(r"[-+]?\d*\.\d+|\d+", str(val))
    if m:
        try:
            return float(m.group(0).replace(',', ''))
        except:
            return np.nan
